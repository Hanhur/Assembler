; Команда безусловного перехода — JMP

; Команда JMP — аналог конструкции GOTO, которая используется в высокоуровневых языках программирования. 
; Название команды объясняет ее действие, а именно «jump», «переход». Команде нужно передать один обязательный операнд — адрес в памяти, 
; с которого процессор должен продолжить выполнение программы. 

; В зависимости от «расстояния» переходы бывают трех типов: короткие
; (short), ближние (near) и дальние (far). Тип перехода задается необязательным
; параметром инструкции jmp. Если тип не задан, по умолчанию используется тип near.

mov ax, 4 ; AX = 4
new_loop: ; метка new_loop
mov bx, ax ; копируем АХ в ВХ

; Чтобы перейти к метке new__loop из другого места программы, используйте команду:
jmp new_loop ;переходим к new_loop
; После выполнения этой команды выполнение программы продолжится с метки new_loop.

jmp start ; переход на start
finish: ; метка "finish" какие-то команды
start:  ; метка "start"
jmp finish ; переход на "finish"

; Если вы не можете оценить правильное «расстояние», все равно можете попробовать указать short — в крайнем случае, компилятор выдаст ошибку:

near_label: ; метка "near_label"
jmp short near_label ; переходим к "near_label"

