; Команды СМР и TEST

; Команды СМР и TEST используются для сравнения двух операндов. Операндами могут быть как регистры, так и адреса памяти, размер операнда — 8, 16 или 32 бита.

cmp o1, o2 

; Команда СМР — это сокращение от «compare», «сравнить». Она работает
; подобно SUB: операнд о2 вычитается из ol. Результат нигде не сохраняется,
; команда просто изменяет регистр признаков. Команда СМР может использоваться как для сравнения целых беззнаковых чисел, 
; так и для сравнения чисел со знаком.

; Команда TEST работает подобно СМР, но вместо вычитания она вычисляет поразрядное И операндов. 
; Результат инструкции — измененные флаги регистра признаков. 
; Мы можем использовать TEST для проверки значений отдельных битов в массиве битов.

cmp ax, 4 ; сравниваем АХ со значением 4
cmp dl, ah ; сравниваем DL с АН
cmp [diameter1], ax ; -сравниваем переменную " diameter1 " с АХ
cmp ax, [diameter1] ; сравниваем АХ с переменной "diameterl"
cmp eax, ecx ; сравниваем регистры ЕАХ и ЕСХ
test ax, 00000100b ; -проверяем значение второго (третьего справа) бита

